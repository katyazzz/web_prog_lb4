<?php
// Генерация случайных чисел N и M
$N = rand(1, 100);
$M = rand($N, $N + 100);

echo "Интервал: от $N до $M<br>";
echo "Целые числа, представимые в виде суммы кубов трех натуральных чисел:<br>";

// Поиск чисел в интервале
for ($i = $N; $i <= $M; $i++) {
    // Перебор возможных комбинаций кубов трех натуральных чисел
    for ($a = 1; $a <= $i; $a++) {
        for ($b = $a; $b <= $i; $b++) {
            for ($c = $b; $c <= $i; $c++) {
                if ($a**3 + $b**3 + $c**3 == $i) {
                    echo "$i = $a^3 + $b^3 + $c^3<br>";
                }
            }
        }
    }
}
?>
