<?php
// Генерация случайных значений для переменных из интервала [-20, 20]
$a = rand(-20, 20);
$b = rand(-20, 20);
$c = rand(-20, 20);
$d = rand(-20, 20);

// Вычисление значения выражения
$result = (($c - ($d / 2)) * ($b - 7)) / ((2 * $a) - 1);

// Вывод формулы и результата
echo "Формула: ((c-(d/2))*(b-7))/(2*a-1)<br>";
echo "Значения переменных: a = $a, b = $b, c = $c, d = $d<br>";
echo "Результат вычислений: $result";
?>
